import sqlite3


def connect_db():
    conn = sqlite3.connect('ativa_esperanca.db')
    return conn


def create_tables():
    conn = connect_db()
    cursor = conn.cursor()

    cursor.execute("""
                   CREATE TABLE IF NOT EXISTS users
                   (
                       id
                       INTEGER
                       PRIMARY
                       KEY
                       AUTOINCREMENT,
                       name
                       TEXT
                       NOT
                       NULL,
                       email
                       TEXT
                       UNIQUE
                       NOT
                       NULL,
                       password
                       TEXT
                       NOT
                       NULL
                   )
                   """)

    cursor.execute("""
                   CREATE TABLE IF NOT EXISTS activities
                   (
                       id
                       INTEGER
                       PRIMARY
                       KEY
                       AUTOINCREMENT,
                       title
                       TEXT
                       NOT
                       NULL,
                       description
                       TEXT,
                       date
                       TEXT,
                       volunteer_id
                       INTEGER,
                       FOREIGN
                       KEY
                   (
                       volunteer_id
                   ) REFERENCES users
                   (
                       id
                   )
                       )
                   """)

    conn.commit()
    conn.close()
